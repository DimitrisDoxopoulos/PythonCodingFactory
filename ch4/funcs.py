def my_add(a, b):
    """
    Adds two numbers and returns the results

    Args:
    a (int, float) -- the first number to add
    b (int, float) -- the second number to add

    Returns
    The sum of a and b
    """
    return a + b

print(my_add(10, 20))
print(my_add(5, 8.3))
print(my_add("Hello ", "Coding Factory"))